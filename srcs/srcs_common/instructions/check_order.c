#include "push_swap.h"

/*
** check if the list is sorted in ascending order
*/

int			check_order(t_lst *l)
{
	while (l && l->next)
	{
		if (l->data > l->next->data)
			return (false);
		l = l->next;
	}
	return (true);
}

/*
** check if stack b is empty and a in order
*/

int			final_checker_order(t_stack *a, t_stack *b)
{
	if (b->size != 0)
		return (false);
	return (check_order(a->first));
}
